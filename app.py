def listen_for_emergency():
    r = sr.Recognizer()
    with sr.Microphone() as source:
        st.info("ЁЯОд Listening... Say 'healthhub emergency' loudly!")
        try:
            audio = r.listen(source, timeout=5, phrase_time_limit=5)
            text = r.recognize_google(audio).lower()
            if "healthhub emergency" in text:
                st.error("ЁЯЪи Emergency Detected! Calling now...")
                # Open phone dialer (works on mobile/web)
                webbrowser.open(f"tel:{EMERGENCY_NUMBER}")
            else:
                st.warning("ЁЯЪи Emergency Detected! Calling now...")
        except Exception as e:
            st.warning(f"Listening failed: {e}")
